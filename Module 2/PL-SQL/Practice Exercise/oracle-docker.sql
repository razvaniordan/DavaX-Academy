CREATE TABLE debug_log (
    log_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    log_time     TIMESTAMP DEFAULT SYSTIMESTAMP,
    module_name  VARCHAR2(100),
    line_no      NUMBER,
    log_message  VARCHAR2(4000),
    session_id   VARCHAR2(50) DEFAULT SYS_CONTEXT('USERENV', 'SESSIONID')
);

--------------------------------------------------------------------------------

CREATE OR REPLACE PACKAGE debug_util AS
    PROCEDURE enable_debug;
    PROCEDURE disable_debug;

    PROCEDURE log(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_log_message IN VARCHAR2
    );

    FUNCTION log_msg(p_message IN VARCHAR2) RETURN VARCHAR2;
    FUNCTION log_variable(p_name  IN VARCHAR2, p_value IN VARCHAR2) RETURN VARCHAR2;
    FUNCTION log_error(p_err  IN VARCHAR2) RETURN VARCHAR2;
    
END debug_util;
/

CREATE OR REPLACE PACKAGE BODY debug_util AS

    g_debug_mode BOOLEAN := FALSE;

    PROCEDURE enable_debug IS
    BEGIN
        g_debug_mode := TRUE;
    END enable_debug;

    PROCEDURE disable_debug IS
    BEGIN
        g_debug_mode := FALSE;
    END disable_debug;

    PROCEDURE log(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_log_message IN VARCHAR2
    ) IS
    BEGIN
        IF g_debug_mode THEN
            INSERT INTO debug_log (
                module_name,
                line_no,
                log_message
            )
            VALUES (
                p_module_name,
                p_line,
                p_log_message
            );
        END IF;
    END log;

    FUNCTION log_msg(
        p_message IN VARCHAR2
    ) RETURN VARCHAR2
    IS
    BEGIN
        RETURN p_message;
    END log_msg;

    FUNCTION log_variable(
        p_name  IN VARCHAR2,
        p_value IN VARCHAR2
    ) RETURN VARCHAR2
    IS
    BEGIN
        RETURN p_name || ' = ' || p_value;
    END log_variable;

    FUNCTION log_error(
        p_err  IN VARCHAR2
    ) RETURN VARCHAR2
    IS
    BEGIN
        RETURN 'ERROR: ' || p_err;
    END log_error;

END debug_util;
/

