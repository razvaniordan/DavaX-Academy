CREATE OR REPLACE PROCEDURE adjust_salaries_by_commission IS

    CURSOR c_emp IS
        SELECT employee_id, salary, commission_pct
        FROM employees2
        FOR UPDATE;

    v_new_salary employees2.salary%TYPE;

BEGIN
    debug_util.log(
        'adjust_salaries_by_commission',
        1,
        debug_util.log_msg('Procedure started')
    );

    FOR r_emp IN c_emp LOOP
        debug_util.log(
            'adjust_salaries_by_commission',
            17,
            debug_util.log_msg('Reading employee_id = ' || TO_CHAR(r_emp.employee_id) || ', salary = ' || TO_CHAR(r_emp.salary) || ', commission = ' || NVL(TO_CHAR(r_emp.commission_pct), 'NULL'))
        );

        IF r_emp.commission_pct IS NOT NULL THEN
            v_new_salary := r_emp.salary + (r_emp.salary * r_emp.commission_pct);

            debug_util.log(
                'adjust_salaries_by_commission',
                25,
                debug_util.log_msg('Commission exists. New salary calculated using commission_pct: ' || TO_CHAR(v_new_salary))
            );
        ELSE
            v_new_salary := r_emp.salary + (r_emp.salary * 0.02);

            debug_util.log(
                'adjust_salaries_by_commission',
                33,
                debug_util.log_msg('Commission is NULL. Applied default 2% increase. New salary: ' || TO_CHAR(v_new_salary))
            );
        END IF;

        UPDATE employees2
        SET salary = v_new_salary
        WHERE CURRENT OF c_emp;

        debug_util.log(
            'adjust_salaries_by_commission',
            42,
            debug_util.log_msg('Updated employee_id = ' || r_emp.employee_id || ' with new salary: ' || TO_CHAR(v_new_salary))
        );
    END LOOP;

    debug_util.log(
        'adjust_salaries_by_commission',
        53,
        debug_util.log_msg('Procedure completed')
    );

    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        debug_util.log(
            'adjust_salaries_by_commission',
            62,
            debug_util.log_error(SQLERRM)
        );
        ROLLBACK;
        RAISE;
END adjust_salaries_by_commission;
/

