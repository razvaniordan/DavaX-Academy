
--pivot
SELECT *
FROM (
    SELECT
        EXTRACT(YEAR FROM v.data_vanzare) AS an,
        a.moneda,
        v.cantitate * a.pret AS suma_incasata
    FROM
        Vanzari v
        JOIN Articole a ON v.articol_id = a.articol_id
    WHERE
        a.moneda IN ('RON', 'EUR', 'USD')
)
PIVOT (
    SUM(suma_incasata)
    FOR moneda IN ('RON' AS RON, 'EUR' AS EUR, 'USD' AS USD)
)
ORDER BY an;

--grouping sets
SELECT 
    TO_CHAR(v.data_vanzare, 'YYYY') AS an,
    TO_CHAR(v.data_vanzare, 'MM') AS luna,
    TO_CHAR(v.data_vanzare, 'Q') AS trimestru,
    a.moneda,
    SUM(v.cantitate*a.pret) AS total_cantitate
FROM Vanzari v
 JOIN Articole a ON v.articol_id = a.articol_id
GROUP BY GROUPING SETS (
    (TO_CHAR(v.data_vanzare, 'YYYY'), TO_CHAR(v.data_vanzare, 'MM'), a.moneda),      -- lunar + an + moneda
    (TO_CHAR(v.data_vanzare, 'YYYY'), TO_CHAR(v.data_vanzare, 'Q'), a.moneda),       -- trimestru + an + moneda
    (TO_CHAR(v.data_vanzare, 'YYYY'), a.moneda)                                     -- an + moneda
)
ORDER BY an, luna, trimestru, moneda;
 
--analytics
SELECT *
FROM (
    SELECT
        v.vanzare_id,
        a.articol_id,
        a.nume AS articol_nume,
        a.moneda,
        v.data_vanzare,
        v.cantitate,
        a.pret,
        v.cantitate * a.pret AS valoare,
        RANK() OVER (
            PARTITION BY a.moneda
            ORDER BY v.cantitate * a.pret DESC
        ) AS rnk
    FROM
        Vanzari v
        JOIN Articole a ON v.articol_id = a.articol_id
    WHERE
        v.data_vanzare >= TRUNC(ADD_MONTHS(SYSDATE, -1), 'MM')  -- prima zi a lunii trecute
    AND
        v.data_vanzare < TRUNC(SYSDATE, 'MM')                   -- prima zi a lunii curente
)
WHERE rnk <= 10
ORDER BY moneda, rnk;

