--hierarchical

SELECT employee_id,
       first_name,
       last_name,
       manager_id,
       LEVEL AS hierarchy_level
FROM employees
START WITH manager_id IS NULL
CONNECT BY PRIOR employee_id = manager_id;


WITH employee_hierarchy (employee_id, first_name, last_name, manager_id, hierarchy_level) AS (
    SELECT 
        employee_id,
        first_name,
        last_name,
        manager_id,
        1 AS hierarchy_level
    FROM employees
    WHERE manager_id IS NULL

    UNION ALL

    SELECT 
        e.employee_id,
        e.first_name,
        e.last_name,
        e.manager_id,
        eh.hierarchy_level + 1
    FROM employees e
    JOIN employee_hierarchy eh ON e.manager_id = eh.employee_id
)
SELECT * FROM employee_hierarchy;

